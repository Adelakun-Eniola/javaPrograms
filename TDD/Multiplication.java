public class Multiplication{

	public static void main(String [] args){
		int num1 = 2;	
		int num2 = -3;
	

	System.out.print(multiply(num1, num2));
	}

	public static int multiply(int num1, int num2){
		int total = 0;
		for(int index = 0; index> num2; index++){
			total = total + num1;
		}
		return total;
	}
	
}
